package day0323;

public class Ferrari implements Car {

    public void turnOn() {
       System.out.println("페라리가 시동을 킵니다.");
        
    }

    public void speedUp() {
        System.out.println("페라리가 속도를 냅니다.");
        
    }


    public void speedDown() {
        System.out.println("페라리가 속도를 줄입니다.");
    }

 
    public void goLeft() {
        System.out.println("페라리가 왼쪽으로 갑니다.");
    }


    public void goRight() {
        System.out.println("페라리가 오른쪽으로 갑니다.");
    }


    public void park() {
        System.out.println("페라리가 주차를 합니다.");
    }


    public void turnOff() {
        System.out.println("페라리가 시동을 끕니다.");
        
    }
    

}
